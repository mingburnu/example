package com.gis;

public class USER {
	private int ID;
	private String NAME;
	private String EMAIL;

	private int getID() {
		return ID;
	}

	public void setID(int value) {
		ID = value;
	}

	public String getNAME() {
		return NAME;
	}

	public void setNAME(String value) {
		NAME = value;
	}

	public String getEMAIL() {
		return EMAIL;
	}

	public void setEMAIL(String value) {
		EMAIL = value;
	}
}